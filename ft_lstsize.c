#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int	size;

	if (!lst)
		return (0);
	size = 0;
	while (lst)
	{
		if (size == INT_MAX)
			return (-1);
		size++;
		lst = lst->next;
	}
	return (size);
}
